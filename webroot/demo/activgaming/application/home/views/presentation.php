	<h3>Nos Origines</h3>

	<p>Début 2016, une poignée de  joueurs ont l’idée de jouer en groupe et de structurer ce dernier.</p><br/>

	<blockquote>Le principe étant de toujours trouver quelqu’un avec qui jouer quelque soit le jeu.</blockquote>

	<p>A l’origine, la communauté s’est principalement structuré autour de League of Legends, jeu qui rassemble aujourd’hui encore un certain pourcentage de nos membres.</p><br/>

	<p>L’un d’entre eux, ayant des connaissances en développement web et réseaux suffisante s’est lancé dans la création d’un site web et d’un support vocal afin d’officialiser cette nouvelle structure.</p><br/>

	<p>C’est Dekadmin qui mit en place les fondations et qui aujourd’hui encore travail d’arrache pied à l’avancée de la communauté et de ses supports. Il est très vite épaulé dans sa tâche par Darcks28 qui participe encore aujourd’hui au développement de la communauté.</p>

	<div class="breaking-line"></div>

	<!-- DESCRIPTION BEGIN TEXT -->
	<h3>Pour qui ?</h3>

	<p>La communauté s'adresse à tous types de joueurs sans niveau de jeu minimum ou temps de connection obligatoire.</p>

